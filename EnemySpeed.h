#ifndef ENEMY_SPEED_H
#define ENEMY_SPEED_H

#include "Enemy.h"

//==============================================================================
// スピード型エネミークラス
//
// 特徴
// ・Enemyを継承し、低HPと高速移動を持つ機動型エネミー
// ・AISpeed設定・HPのみ基底クラスと異なる
//==============================================================================
class EnemySpeed : public Enemy
{
public:
    static constexpr float SIGHT_DIST     = 12.0f;  // 視野距離（スピード型は鋭い）
    static constexpr int   HP             = 120;   // 最大HP（通常の0.4倍）
    static constexpr float CHASE_SPD      = 4.0f;  // 追跡速度（m/s）
    static constexpr float PATROL_SPD     = 2.0f;  // 巡回速度（m/s）
    static constexpr float MAX_SPEED_MULT = 2.5f;  // 最大速度の倍率（ジェット速度）

    //==========================================================================
    // 初期化処理
    //
    // 引数
    // ・position : 初期位置
    //==========================================================================
    void Initialize(const DirectX::XMFLOAT3& position) override;

    //==========================================================================
    // 更新処理
    //
    // 引数
    // ・elapsed_time : 前フレームからの経過時間（秒）
    //==========================================================================
    void Update(double elapsed_time) override;

    //==========================================================================
    // 描画処理
    //==========================================================================
    void Draw() override;

    //==========================================================================
    // 討伐スコア
    //==========================================================================
    int GetKillScore() const override { return 1500; }
};

#endif // ENEMY_SPEED_H
