/*==============================================================================

   プレイヤー制御 [player.h]
                                                         Author : 51106
                                                         Date   : 2025/12/19
--------------------------------------------------------------------------------
   プレイヤーの移動・射撃・HP・当たり判定を管理するモジュールのパブリック API。

   ■主な機能
     - 移動 / ジャンプ / 重力処理（WASD + パッド）
     - マウス / スティックによる視点回転
     - 弾発射（PlayerWeapon 経由）
     - OBB ↔ AABB による壁・床の衝突判定
     - HP 管理・無敵時間・ダメージ受け付け
     - ロックオン対象位置の取得

   ■OBB 情報
     Player_GetOBB() でプレイヤーの OBB（向き込みの当たり判定）を取得できる。
     エネミーの衝突判定や弾の当たり判定に使用する。

==============================================================================*/
#ifndef PLAYER_H
#define PLAYER_H

#include <DirectXMath.h>
#include "collision.h"
#include "collision_obb.h"

//==============================================================================
// 初期化：位置と向きを設定し、モデルを読み込む
//
// ■役割
// ・プレイヤーの初期位置・向きを設定
// ・モデル読み込み
// ・マウスモード設定
// ・HP初期化
//
// ■引数
// ・position : 初期位置
// ・front    : 初期向き（正面ベクトル）
//==============================================================================;
void Player_Initialize(const DirectX::XMFLOAT3& position, const DirectX::XMFLOAT3 front);

//==============================================================================
// 終了処理：モデル解放
//
// ■役割
// ・プレイヤーモデルの解放
//==============================================================================
void Player_Finalize();

//==============================================================================
// 更新処理：移動・ジャンプ・重力・当たり判定・弾発射など
//
// ■役割
// ・入力処理（WASD/パッド/マウス）
// ・重力・ジャンプ処理
// ・壁/床との衝突判定（OBB ↔ AABB）
// ・弾発射
// ・無敵時間の更新
//
// ■引数
// ・elapsed_time : 前フレームからの経過時間（秒）
//==============================================================================
void Player_Update(double elapsed_time);

//==============================================================================
// 描画処理：ライト設定＋モデル描画
//
// ■役割
// ・スペキュラライト設定
// ・モデルのワールド変換＋描画
// ・ダメージ時の点滅表現
//==============================================================================
void Player_Draw();

void Player_DrawMarker();//エッジ化に伴い分離

//==============================================================================
// 現在のプレイヤー OBB を返す
//
// ■役割
// ・プレイヤーの位置・向き・サイズから OBB を生成して返す
//
// ■戻り値
// ・プレイヤーの OBB
//==============================================================================
OBB Player_GetOBB();

//==============================================================================
// 任意の位置ベクトルからプレイヤー用 OBB を作成
//
// ■役割
// ・衝突判定用の一時計算に使用
// ・現在の向き（g_PlayerFront）を使って OBB を生成
//
// ■引数
// ・position : 中心座標（XMVECTORで渡す）
//
// ■戻り値
// ・指定位置でのプレイヤーOBB
//==============================================================================
OBB Player_ConvertPositionToOBB(const DirectX::XMVECTOR& position);

//==============================================================================
// AABB互換取得（デバッグ用・旧コード互換用）
//
// ■役割
// ・プレイヤーの現在位置を軸平行なAABBとして返す
// ・回転は考慮されない（デバッグ表示用）
//
// ■戻り値
// ・軸平行なAABB
//==============================================================================
AABB Player_GetAABB();

//==============================================================================
// プレイヤーの現在位置・向き取得
//
// ■役割
// ・プレイヤーの現在座標を返す
//
// ■戻り値
// ・座標（XMFLOAT3の参照）
//==============================================================================
const DirectX::XMFLOAT3& Player_GetPosition();

//==============================================================================
// プレイヤーの現在の向き取得
//
// ■役割
// ・プレイヤーの正面ベクトルを返す
//
// ■戻り値
// ・正面ベクトル（XMFLOAT3の参照）
//==============================================================================
const DirectX::XMFLOAT3& Player_GetFront();

//==============================================================================
// 当たり判定寸法を取得
//
// ■役割
// ・プレイヤーのOBBサイズを取得する
//==============================================================================
float Player_GetHalfWidthX();
float Player_GetHalfWidthZ();
float Player_GetHeight();

//==============================================================================
// 生存/操作可フラグ
//
// ■役割
// ・プレイヤーが操作可能かどうかを返す
//
// ■戻り値
// ・true  : 操作可能
// ・false : 無効化（死亡・イベント中など）
//==============================================================================
bool Player_IsEnable();

//==============================================================================
// 生存/操作可フラグ設定
//
// ■役割
// ・プレイヤーの有効/無効を切り替える
// ・無効化時は速度・ジャンプフラグもリセット
//
// ■引数
// ・enable : true=有効、false=無効
//==============================================================================
void Player_SetEnable(bool enable);

//==============================================================================
// シャドウパス用深度描画（ShadowMap::BeginPass〜EndPass の間で呼ぶ）
//==============================================================================
void Player_DrawShadow();

//==============================================================================
// スラスターパーティクルを全消去（ルーム遷移時用）
//==============================================================================
void Player_ClearParticles();

//==============================================================================
// プレイヤー位置を強制設定
//
// ■役割
// ・生成マップに合わせて安全位置へ移動させる用途
// ・速度・ジャンプフラグもリセット
//
// ■引数
// ・position   : 新しい座標
// ・setAsStart : true の場合、リスポーン位置としても記録
//==============================================================================
void Player_SetPosition(const DirectX::XMFLOAT3& position, bool setAsStart = false);

//==============================================================================
// プレイヤー正面方向をセット（マップ遷移時などに向きを初期化する用）
//==============================================================================
void Player_SetFront(const DirectX::XMFLOAT3& front);

//==============================================================================
// プレイヤー速度ポインタ取得（ノックバック用）
//
// ■役割
// ・エネミーとの衝突時にノックバックを適用するため、速度を直接書き換える
//
// ■戻り値
// ・速度ベクトルのポインタ
//==============================================================================
DirectX::XMFLOAT3* Player_GetVelocityPtr();

//==============================================================================
// HP関連
//==============================================================================

//==============================================================================
// ダメージを受ける
//
// ■役割
// ・プレイヤーのHPを減らす
// ・無敵時間中はダメージを受けない
// ・HPが0になったら死亡処理（Player_SetEnable(false)）
//
// ■引数
// ・damage : 受けるダメージ量
//
// ■戻り値
// ・true  : ダメージを受けた
// ・false : 無敵時間中でダメージを受けなかった
//==============================================================================
bool Player_TakeDamage(int damage);

//==============================================================================
// 現在のHP取得
//
// ■役割
// ・プレイヤーの現在HPを返す
//
// ■戻り値
// ・現在のHP
//==============================================================================
int Player_GetHP();

//==============================================================================
// 最大HP取得
//
// ■役割
// ・プレイヤーの最大HPを返す
//
// ■戻り値
// ・最大HP
//==============================================================================
int Player_GetMaxHP();

//==============================================================================
// HP回復
//
// ■役割
// ・プレイヤーのHPを回復する（最大値を超えない）
//
// ■引数
// ・amount : 回復量
//==============================================================================
void Player_Heal(int amount);

//==============================================================================
// HPリセット
//
// ■役割
// ・プレイヤーのHPを最大値に戻す
// ・ダンジョン再生成時などに使用
//==============================================================================
void Player_ResetHP();

//==============================================================================
// 無敵状態判定
//
// ■役割
// ・プレイヤーが現在無敵状態かを返す
//
// ■戻り値
// ・true  : 無敵状態
// ・false : 通常状態
//==============================================================================
bool Player_IsInvincible();


//==============================================================================
// 攻撃力倍率取得
//==============================================================================
float Player_GetDamageMultiplier();


//==============================================================================
// 攻撃力倍率設定
//==============================================================================
void Player_SetDamageMultiplier(float multiplier);

//==============================================================================
// ビームエネルギー取得
//==============================================================================
float Player_GetBeamEnergy();

//==============================================================================
// ビームエネルギー最大値取得
//==============================================================================
float Player_GetBeamEnergyMax();

//==============================================================================
// ビームエネルギー回復（アイテム使用時などに呼ぶ）
// amount=回復量
//==============================================================================
void Player_AddBeamEnergy(float amount);


float Player_GetSpeedMultiplier();
void  Player_SetSpeedMultiplier(float multiplier);

//==============================================================================
// 通常スロット武器インデックスを設定（WeaponSelect から呼ぶ）
//
// ■引数
// ・idx : 0=Normal  1=Shotgun  2=Missile
//==============================================================================
void Player_SetNormalWeaponIndex(int idx);
void Player_SetLeftWeaponIndex(int idx);   // 左腕武器を設定（WeaponID）
int  Player_GetNormalWeaponIndex();        // 現在の右腕武器インデックスを取得
int  Player_GetLeftWeaponIndex();          // 現在の左腕武器インデックスを取得


#endif // PLAYER_H