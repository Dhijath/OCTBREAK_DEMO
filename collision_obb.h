#ifndef COLLISION_OBB_H
#define COLLISION_OBB_H

#include <DirectXMath.h>
#include "collision.h"

//==============================================================================
// OBB構造体（Y軸回転のみ）
//==============================================================================
struct OBB
{
    DirectX::XMFLOAT3 center;      // 中心座標
    DirectX::XMFLOAT3 halfExtents; // 各軸の半サイズ（ローカル空間）X/Y/Z
    DirectX::XMFLOAT3 axisX;       // X軸方向ベクトル（ワールド空間、正規化済み）
    DirectX::XMFLOAT3 axisY;       // Y軸方向ベクトル（通常は (0,1,0)）
    DirectX::XMFLOAT3 axisZ;       // Z軸方向ベクトル（ワールド空間、正規化済み）

    //==========================================================================
    // デフォルトコンストラクタ（AABBと同じ向き）
    //==========================================================================
    OBB()
        : center{ 0.0f, 0.0f, 0.0f }
        , halfExtents{ 0.5f, 0.5f, 0.5f }
        , axisX{ 1.0f, 0.0f, 0.0f }
        , axisY{ 0.0f, 1.0f, 0.0f }
        , axisZ{ 0.0f, 0.0f, 1.0f }
    {
    }

    //==========================================================================
    // 位置・サイズ・向き（front）から生成
    //
    // ■役割
    // ・キャラクターの位置・サイズ・向きベクトルからOBBを構築する
    //
    // ■引数
    // ・position   : 中心座標（足元ではなく、AABBの中心に相当する位置）
    // ・halfExtents: 各軸の半サイズ（X/Y/Z）
    // ・front      : 正面方向ベクトル（XZ平面、正規化不要）
    //
    // ■戻り値
    // ・構築されたOBB
    //==========================================================================
    static OBB CreateFromFront(
        const DirectX::XMFLOAT3& position,
        const DirectX::XMFLOAT3& halfExtents,
        const DirectX::XMFLOAT3& front);

    //==========================================================================
    // AABBから生成（回転なし）
    //
    // ■役割
    // ・既存のAABBをOBBとして扱う（軸は標準のXYZ）
    //
    // ■引数
    // ・aabb : 変換元のAABB
    //
    // ■戻り値
    // ・AABBと同じ形状・位置のOBB（回転なし）
    //==========================================================================
    static OBB CreateFromAABB(const AABB& aabb);
};

//==============================================================================
// OBB同士の重なり判定
//
// ■役割
// ・分離軸定理（SAT）を用いてOBB同士の衝突を判定する
// ・Y軸回転のみのため、判定軸は4本（各OBBのX/Z軸）
//
// ■引数
// ・a, b : 判定対象のOBB
//
// ■戻り値
// ・true  : 重なっている
// ・false : 重なっていない
//==============================================================================
bool Collision_IsOverlapOBB(const OBB& a, const OBB& b);

//==============================================================================
// OBBとAABBの重なり判定
//
// ■役割
// ・OBBと軸平行なAABBの衝突を判定する
// ・AABBを一時的にOBBに変換して判定
//
// ■引数
// ・obb  : OBB
// ・aabb : AABB
//
// ■戻り値
// ・true  : 重なっている
// ・false : 重なっていない
//==============================================================================
bool Collision_IsOverlapOBB_AABB(const OBB& obb, const AABB& aabb);

//==============================================================================
// OBB同士の衝突判定（押し出し方向付き）
//
// ■役割
// ・重なりの有無だけでなく、押し出し方向（法線）も返す
// ・キャラクター同士の衝突解決に使用
//
// ■引数
// ・a, b : 判定対象のOBB
//
// ■戻り値
// ・Hit構造体
//   - isHit  : 衝突の有無
//   - normal : bからaを押し出す方向（正規化済み）
//==============================================================================
Hit Collision_IsHitOBB(const OBB& a, const OBB& b);

//==============================================================================
// OBBとAABBの衝突判定（押し出し方向付き）
//
// ■役割
// ・OBBとAABBの衝突で、OBBを押し出す方向を返す
// ・マップ（AABB）との衝突解決に使用
//
// ■引数
// ・obb  : OBB
// ・aabb : AABB（マップの壁など）
//
// ■戻り値
// ・Hit構造体
//   - isHit  : 衝突の有無
//   - normal : AABBからOBBを押し出す方向（正規化済み）
//==============================================================================
Hit Collision_IsHitOBB_AABB(const OBB& obb, const AABB& aabb);

//==============================================================================
// デバッグ描画：OBB（線で囲う）
//
// ■役割
// ・OBBの8頂点を計算し、12本の線で描画する
//
// ■引数
// ・obb   : 描画対象のOBB
// ・color : 線の色
//==============================================================================
void Collision_DebugDraw(const OBB& obb, const DirectX::XMFLOAT4& color);

//==============================================================================
// レイセグメントとOBBの交差判定
//
// ■役割
// ・from から to への線分がOBBと交差するか判定する
// ・弾のすり抜け防止（前フレーム位置→現在位置の経路チェック）に使用
//
// ■引数
// ・obb  : 判定対象のOBB
// ・from : 線分の始点（前フレームの弾位置）
// ・to   : 線分の終点（現在フレームの弾位置）
//
// ■戻り値
// ・true  : 線分がOBBと交差している（命中）
// ・false : 交差していない
//==============================================================================
bool OBB_RaySegmentIntersect(const OBB& obb, const DirectX::XMFLOAT3& from, const DirectX::XMFLOAT3& to);

#endif // COLLISION_OBB_H