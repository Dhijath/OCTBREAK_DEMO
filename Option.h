/*==============================================================================

   オプション画面 [Option.h]
                                                         Author : 51106
                                                         Date   : 2026/03/23
--------------------------------------------------------------------------------

   ゲームのオプション設定を行う画面のパブリック API。
   Game_Manager から Initialize / Finalize / Update / Draw を呼ぶことで動作する。

   ■設定項目
     - VOLUME     : マスター音量（0〜100%、0.1 刻み）
     - FULLSCREEN : ボーダーレスフルスクリーン ↔ ウィンドウ の切替

   ■操作
     UP / DOWN (W/S / 十字↑↓)    : 項目選択
     LEFT / RIGHT (A/D / 十字←→) : 値の変更
     ENTER / PAD_B                : タイトルへ戻る

   ■画面スタイル
     WeaponSelect と同スタイル（DebugText + Sprite）。
     フルスクリーン切替は次フレーム先頭に遅延実行される（GPU フリーズ防止）。

   ■状態の永続性
     Volume は Option_Finalize() 後も内部変数として保持され、
     次回 Option_Initialize() 時にそのまま引き継がれる。

==============================================================================*/

#ifndef OPTION_H
#define OPTION_H

/*------------------------------------------------------------------------------
  初期化
  - テクスチャ・DebugText・SE をロードする
  - Volume は初回のみデフォルト値（50%）で初期化し、2 回目以降は前回値を維持
  - GameManager から Option 遷移時に 1 度だけ呼ぶ
------------------------------------------------------------------------------*/
void Option_Initialize();

/*------------------------------------------------------------------------------
  終了処理
  - ロードしたテクスチャ・DebugText・SE を解放する
  - Volume の値はリセットしない（永続）
  - GameManager がオプション状態を抜けるときに呼ぶ
------------------------------------------------------------------------------*/
void Option_Finalize();

/*------------------------------------------------------------------------------
  毎フレーム更新
  - 入力を読んで選択項目・音量・フルスクリーン状態を更新する
  - フルスクリーン切替は即時実行せず、GameWindow_RequestFullscreenToggle() で
    次フレーム先頭への遅延実行を登録する（GPU フリーズ防止）
  引数:
    elapsed_time - 前フレームからの経過時間（秒）
------------------------------------------------------------------------------*/
void Option_Update(double elapsed_time);

/*------------------------------------------------------------------------------
  毎フレーム描画
  - 背景・パネル・バー（Sprite_Draw）とテキスト（DebugText）を描画する
  - DebugText::Draw() 後に Sprite_Begin() を再呼び出しして
    後続の Fade_Draw() のパイプライン状態を保護する
------------------------------------------------------------------------------*/
void Option_Draw();

/*------------------------------------------------------------------------------
  オプション終了フラグの取得
  - ENTER / PAD_B が押されたフレームだけ true を返す（ワンショット）
  - 連続して true になることはない
  戻り値:
    true  - タイトルへ戻るべきフレーム
    false - 継続
------------------------------------------------------------------------------*/
bool Option_IsEnd();

/*------------------------------------------------------------------------------
  シャドウモード（game.cpp で参照）
    0 = なし
    1 = 低（マル影）
    2 = 中（シャドウマップ・1サンプル）
    3 = 高（シャドウマップ・PCF 3x3）  ← デフォルト
------------------------------------------------------------------------------*/
int  Option_GetShadowMode();
void Option_SetShadowMode(int mode);

#endif // OPTION_H
