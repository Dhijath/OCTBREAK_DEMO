#ifndef MAP_H
#define MAP_H

#include <DirectXMath.h>
#include "collision.h"
#include <vector>
#include <cstdint>

//==============================================================================
// マップオブジェクト構造体
//==============================================================================
struct MapObject
{
    int KindId;                 // 種別ID（床/壁/天井など）
    DirectX::XMFLOAT3 Position; // 位置
    AABB Aabb;                  // 当たり判定（AABB）
};

//==============================================================================
// 初期化
//
// ■役割
// ・テクスチャ読み込み
// ・初期ダンジョン生成
//==============================================================================
void Map_Initialize();

//==============================================================================
// 終了処理
//
// ■役割
// ・リソース解放
//==============================================================================
void Map_Finalize();

//==============================================================================
// ダンジョン生成
//
// ■役割
// ・指定シードで新しいダンジョンを生成する
//
// ■引数
// ・seed : 乱数シード（同じシードなら同じマップ）
//==============================================================================
void Map_GenerateDungeon(std::uint32_t seed);

//==============================================================================
// 床登録（FloorRegistry用）
//
// ■役割
// ・床AABBをFloorRegistryに登録する
//==============================================================================
void Map_RegisterFloors();

//==============================================================================
// 描画
//
// ■役割
// ・マップ全体の描画
//==============================================================================
void Map_Draw();

//==============================================================================
// ゴールビルボード描画（不透明モデルをすべて描いた後に呼ぶこと）
// ・透過越しにエネミー・プレイヤーが正しく映るよう、描画順を分離
//==============================================================================
void Map_DrawGoal();

//==============================================================================
// 天井テクスチャ差し替え
//
// ■役割
// ・天井のテクスチャIDを変更する
//
// ■引数
// ・texId : 新しいテクスチャID
//==============================================================================
void Map_SetCeilingTexture(int texId);

//==============================================================================
// エネミースポーン位置取得
//
// ■役割
// ・ダンジョン生成時に決定されたエネミースポーン座標リストを返す
//
// ■戻り値
// ・スポーン位置リスト（const参照）
//==============================================================================
const std::vector<DirectX::XMFLOAT3>& Map_GetEnemySpawnPositions();

//==============================================================================
// プレイヤースポーン位置取得
//
// ■役割
// ・ダンジョン生成時に決定されたプレイヤースポーン位置を返す
//
// ■戻り値
// ・スポーン座標
//==============================================================================
DirectX::XMFLOAT3 Map_GetSpawnPosition();

//==============================================================================
// ボスルーム中心座標取得
//
// ■役割
// ・ダンジョン生成時に確定したボスのスポーン位置を返す
// ・game.cpp から EnemyBoss をスポーンするために使用する
//
// ■戻り値
// ・ボスルーム中心座標
//==============================================================================
DirectX::XMFLOAT3 Map_GetBossSpawnPosition();

//==============================================================================
// ゴール位置取得
//
// ■役割
// ・ゴールの座標を返す
//
// ■戻り値
// ・ゴール座標
//==============================================================================
DirectX::XMFLOAT3 Map_GetGoalPosition();

//==============================================================================
// プレイヤーがゴールに到達したか判定
//
// ■役割
// ・プレイヤーのAABBとゴールのAABBが重なっているかチェックする
//
// ■戻り値
// ・true  : ゴール到達
// ・false : 未到達
//==============================================================================
bool Map_IsPlayerReachedGoal();

//==============================================================================
// ゴール到達回数を加算
//
// ■役割
// ・ゴールに到達したときに呼び出す
// ・内部カウンタを+1する
//==============================================================================
void Map_AddGoalReachCount();

//==============================================================================
// ゴール到達回数を取得
//
// ■役割
// ・現在のゴール到達回数を返す
//
// ■戻り値
// ・到達回数
//==============================================================================
int Map_GetGoalReachCount();

//==============================================================================
// ゴール到達回数をリセット
//
// ■役割
// ・カウンタを0に戻す（マップ再生成時など）
//==============================================================================
void Map_ResetGoalReachCount();

//==============================================================================
// クリア判定
//
// ■役割
// ・ゴール到達回数が目標回数以上かチェック
//
// ■戻り値
// ・true  : クリア条件達成
// ・false : 未達成
//==============================================================================
bool Map_IsClearConditionMet();

void Map_Light_Reset();

//==============================================================================
// MapObject数取得
//
// ■役割
// ・現在保持しているMapObjectの総数を返す
//
// ■戻り値
// ・MapObject数
//==============================================================================
int Map_GetObjectsCount();

//==============================================================================
// MapObject取得
//
// ■役割
// ・指定インデックスのMapObjectを返す
//
// ■引数
// ・index : インデックス
//
// ■戻り値
// ・MapObjectのポインタ
//==============================================================================
const MapObject* Map_GetObject(int index);

// Map_Initialize() などの宣言がある場所に追加する用
std::uint32_t Map_GenerateRandomSeed();

//==============================================================================
// ボス部屋専用マップ生成
//
// ■役割
// ・2回ゴール到達後に遷移するボス専用アリーナ（単一の大部屋）を生成する
// ・通常ダンジョンの代わりに呼ぶ
//
// ■引数
// ・seed : 乱数シード（現在は未使用だが将来の拡張用）
//==============================================================================
void Map_GenerateBossRoom(std::uint32_t seed);

// map.h
void Map_DrawMinimap();  // ミニマップ専用


// テクスチャID取得関数
int Map_GetFloorTexID();
int Map_GetWallTexID();
int Map_GetCeilingTexID();
int Map_GetWiteTexID();

//==============================================================================
// 壁レイキャスト
//
// ■役割
// ・start から end の線分と壁AABBの交差判定を行う
// ・交差した場合、最も start に近い交差点を outHitPos に書き込む
//
// ■引数
// ・start     : 線分の始点（カメラ基準点）
// ・end       : 線分の終点（カメラの理想位置）
// ・outHitPos : 最近交差点の受け取りポインタ
//
// ■戻り値
// ・true  : 交差あり
// ・false : 交差なし
//==============================================================================
bool Map_RaycastWalls(
    const DirectX::XMFLOAT3& start,
    const DirectX::XMFLOAT3& end,
    DirectX::XMFLOAT3* outHitPos,
    bool wallsOnly = false);   // true = KIND_WALL のみ（床・天井を無視）


//==============================================================================
// 壁越しでないか判定
//
// ■役割
// ・from から to へ壁レイキャストを飛ばし、途中に壁/床/天井があれば false
// ・ロックオン継続判定などに使用する
//
// ■引数
// ・from : 判定開始位置
// ・to   : 判定終了位置
//
// ■戻り値
// ・true  : 視線が通っている
// ・false : 途中に遮蔽物がある
//==============================================================================
bool Map_HasLineOfSight(
    const DirectX::XMFLOAT3& from,
    const DirectX::XMFLOAT3& to,
    bool wallsOnly = false);   // true = 壁のみ判定（床・天井を無視）

void Map_DrawForMinimap();

#endif // MAP_H