#ifndef ENEMY_SNIPER_H
#define ENEMY_SNIPER_H

#include "Enemy.h"

//==============================================================================
// スナイパー型エネミークラス
//
// 特徴
// ・Enemyを継承し、遠距離から射撃を行うエネミー
// ・プレイヤーを視野に捉えた際、一定間隔でEnemyBulletを発射する
// ・射程内ではその場で射撃し、射程外では追跡する
//==============================================================================
class EnemySniper : public Enemy
{
public:
    static constexpr float SIGHT_DIST     = 17.0f; // 視野距離（スナイパーは遠距離）
    static constexpr int   HP             = 150;   // 最大HP
    static constexpr float CHASE_SPD      = 1.2f;  // 追跡速度（m/s）
    static constexpr float PATROL_SPD     = 0.8f;  // 巡回速度（m/s）
    static constexpr float SHOOT_RANGE    = 8.0f;  // 射撃射程（m）
    static constexpr float SHOOT_INTERVAL = 1.5f;  // 射撃間隔（秒）
    static constexpr int   SHOOT_DAMAGE   = 405;   // 1発のダメージ量

    //==========================================================================
    // 初期化処理
    //
    // 引数
    // ・position : 初期位置
    //==========================================================================
    void Initialize(const DirectX::XMFLOAT3& position) override;

    //==========================================================================
    // 更新処理
    //
    // 引数
    // ・elapsed_time : 前フレームからの経過時間（秒）
    //==========================================================================
    void Update(double elapsed_time) override;

    //==========================================================================
    // 描画処理
    //==========================================================================
    void Draw() override;

private:
    float m_shootTimer = 0.0f; // 射撃クールタイムタイマー（秒）

    //==========================================================================
    // 射撃処理
    //
    // 処理
    // ・プレイヤーへ向けてEnemyBulletを発射する
    //==========================================================================
    void Shoot();

    //==========================================================================
    // プレイヤーまでの距離を取得する
    //
    // 戻り値
    // ・XZ平面上のプレイヤーまでの距離（m）
    //==========================================================================
    float GetDistanceToPlayer() const;
};

#endif // ENEMY_SNIPER_H
